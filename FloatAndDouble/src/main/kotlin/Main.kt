fun main() {
    // Floating point numbers

    // Float 32 bit
    // Double 64 bit

    val myFloatNumber = 2.5f // float
    val mySecondNumber = 2.5 // double assigned automatically
    val myThirdNumber : Double = 2.0

    println(myThirdNumber)

    println("Max value of float is ${Float.MAX_VALUE}")
    println("Min value of float is ${Float.MIN_VALUE}")

    println("Max value of double is ${Double.MAX_VALUE}")
    println("Min value of double is ${Double.MIN_VALUE}")
}